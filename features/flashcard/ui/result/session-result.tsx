"use client";

import { useState } from "react";
import { useSearchParams, useRouter } from "next/navigation";
import { countBy } from "@/shared/lib/array-utils";
import { FLASHCARD_RESULT_MESSAGES, FLASHCARD_ROUTES, FLASHCARD_STORAGE_KEYS } from "../../config";
import type { MasteryLevel, SessionResult } from "../../types";
import { MasteryBreakdown } from "./mastery-breakdown";
import { StatsGrid } from "./stats-grid";

function getResultHeader(accuracy: number) {
  return (
    FLASHCARD_RESULT_MESSAGES.find((entry) => accuracy >= entry.minAccuracy) ??
    FLASHCARD_RESULT_MESSAGES[FLASHCARD_RESULT_MESSAGES.length - 1]
  );
}

export function SessionResultContent() {
  const searchParams = useSearchParams();
  const router = useRouter();
  const [sessionResult] = useState<SessionResult>(() => {
    if (typeof window !== "undefined") {
      const storedResult = sessionStorage.getItem(FLASHCARD_STORAGE_KEYS.result);
      if (storedResult) {
        try {
          const parsed = JSON.parse(storedResult) as SessionResult;
          sessionStorage.removeItem(FLASHCARD_STORAGE_KEYS.result);
          return parsed;
        } catch (error) {
          console.error("Failed to parse flashcard result:", error);
        }
      }
    }

    return {
      xp: Number(searchParams.get("xp") || 0),
      accuracy: Number(searchParams.get("accuracy") || 0),
      total: Number(searchParams.get("total") || 0),
      correct: Number(searchParams.get("correct") || 0),
    };
  });

  const { xp, accuracy, total, correct, results } = sessionResult;
  const resultHeader = getResultHeader(accuracy);

  const masteryBreakdown = (
    results ? countBy(results, (result) => result.masteryLevel) : {}
  ) as Partial<Record<MasteryLevel, number>>;

  const nextReviewDate =
    results && results.length > 0
      ? new Date(Math.min(...results.map((result) => new Date(result.nextReviewDate).getTime())))
      : null;

  return (
    <div className="min-h-screen bg-gradient-to-br from-blue-50 via-purple-50 to-pink-50 px-4 py-8">
      <div className="max-w-4xl mx-auto space-y-6">
        <div className="bg-white rounded-3xl shadow-2xl p-8">
          <div className="text-center space-y-4">
            <p className="text-7xl">{resultHeader.emoji}</p>
            <h1 className="text-3xl font-sans font-bold text-gray-800">{resultHeader.title}</h1>
            <p className="text-gray-600">{resultHeader.motivation}</p>
          </div>
        </div>

        <StatsGrid accuracy={accuracy} xp={xp} correct={correct} total={total} />

        {Object.keys(masteryBreakdown).length > 0 && (
          <MasteryBreakdown breakdown={masteryBreakdown} />
        )}

        {nextReviewDate && (
          <div className="bg-gradient-to-r from-indigo-500 to-purple-600 rounded-3xl p-8 shadow-md text-white">
            <div className="flex items-center gap-4">
              <div className="w-16 h-16 bg-white/20 backdrop-blur-sm rounded-2xl flex items-center justify-center flex-shrink-0">
                <span className="text-3xl">SRS</span>
              </div>
              <div className="flex-1">
                <h3 className="text-xl font-sans font-bold mb-2">Next Review Time</h3>
                <p className="text-lg opacity-90">
                  {nextReviewDate.toLocaleDateString("ko-KR", {
                    month: "long",
                    day: "numeric",
                    hour: "2-digit",
                    minute: "2-digit",
                  })}
                </p>
                <p className="text-sm opacity-75 mt-1">
                  This schedule is generated by the spaced repetition algorithm.
                </p>
              </div>
            </div>
          </div>
        )}

        <div className="bg-white rounded-3xl p-8 shadow-md space-y-3">
          <h2 className="text-xl font-sans font-bold text-gray-800 mb-4">Next Actions</h2>
          <button
            onClick={() => router.push(`${FLASHCARD_ROUTES.session}?mode=review`)}
            className="w-full px-6 py-4 bg-gradient-to-r from-blue-500 to-purple-500 text-white rounded-xl hover:from-blue-600 hover:to-purple-600 font-semibold shadow-lg transition-all hover:scale-105"
          >
            Continue Review
          </button>
          <button
            onClick={() => router.push(`${FLASHCARD_ROUTES.session}?mode=new`)}
            className="w-full px-6 py-4 bg-gradient-to-r from-green-500 to-emerald-500 text-white rounded-xl hover:from-green-600 hover:to-emerald-600 font-semibold shadow-lg transition-all hover:scale-105"
          >
            Learn New Words
          </button>
          <button
            onClick={() => router.push(FLASHCARD_ROUTES.modes)}
            className="w-full px-6 py-4 bg-white border-2 border-gray-300 text-gray-700 rounded-xl hover:bg-gray-50 font-semibold transition-all hover:scale-105"
          >
            Change Study Mode
          </button>
          <button
            onClick={() => router.push(FLASHCARD_ROUTES.home)}
            className="w-full px-6 py-4 bg-white border-2 border-gray-300 text-gray-700 rounded-xl hover:bg-gray-50 font-semibold transition-all hover:scale-105"
          >
            Back to Home
          </button>
        </div>

        <div className="bg-white rounded-3xl p-8 shadow-md">
          <div className="space-y-3 text-sm text-gray-600">
            <h3 className="text-lg font-sans font-bold text-gray-800">Tips</h3>
            <p>Keep sessions short and frequent to strengthen long-term recall.</p>
            <p>Prioritize cards marked as hard or forgot in your next cycle.</p>
            <p>Follow the SRS schedule for consistent improvement.</p>
          </div>
        </div>
      </div>
    </div>
  );
}
